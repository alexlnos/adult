        function lazyLoad() {
            for (var i = 0; i < lazy.length; i++) {
                if (isInViewport(lazy[i])) {
                    if (lazy[i].getAttribute('data-src')) {
                        lazy[i].src = lazy[i].getAttribute('data-src');
                        lazy[i].removeAttribute('data-src');
                    }
                }
            }

