                  <p> Ereksi pada waktu pagi adalah suatu keharusan. Ia boleh bertahan dari 15 minit hingga 2 jam, tanpa mengira umur anda. Salah satu sebab penampilan zakar adalah tahap testosteron, yang mengisi zakar dengan darah. </p>
                </li>
                <li>
                  <p> Ereksi yang gah. Jika setiap hubungan seksual gagal kerana zakar anda tidak cukup keras, ini juga merupakan suatu sebab untuk menjadi bimbang. </p>
                </li>
                <li>
                  <p> Masa ejakulasi ditangguhkan. Biasanya ia sepatutnya tidak berlaku lebih awal dari 5 minit, seorang lelaki sepatutnya dapat menangguhkan perasaannya. </p>
                </li>
                <li>
                  <p> Jika terdapat lebih dari 30 minit sehingga ereksi seterusnya, ini juga akan menjadi suatu sebab untuk rasa bimbang. </p>
                </li>
                <li>
                  <p> Ereksi setiap hari. Ya, jika anda sihat, ia sepatutnya muncul setiap hari, walaupun anda tidak akan melakukan seks. </p>
