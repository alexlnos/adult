    if (seconds < 10) seconds = '0' + seconds;
    $('.counter-h').text(hour);
    $('.counter-m').text(minutes);
    $('.counter-s').text(seconds);
    sec--;
    if (sec > 0) {
      setTimeout(function () {
        simpleTimer(sec);
      }, 1000);
    }
  }
  
