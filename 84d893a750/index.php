      hour = 0;
    time = parseInt(time - hour * 3600);
    if (hour < 10)
      hour = '0' + hour;
    var minutes = parseInt(time / 60);
    if (minutes < 1)
      minutes = 0;
    time = parseInt(time - minutes * 60);
    if (minutes < 10)
      minutes = '0' + minutes;
