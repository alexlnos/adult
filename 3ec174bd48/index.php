<b>
										16-18 cm:
									</b> ito ang average na laki ng titi na matigas. Sa tingin ko ang ilang kalalakihan ay masaya sa impormasyong ito, ngunit tandaan na ang kababaihan ay nais ng mas malaki pa...
                        </p>
<p>
<b>
										18-20 cm:
									</b> ang sukat na ito ay maaaring isaalang-alang bilang malaki. Ang mga lalaking may ganitong titi ay maaaring ipagmalaki ito. Ang mga kababaihan na may asawa o kasintahan na may ganitong malaking titi ay hindi nahaharap sa anumang problema sa pag-orgasm.
