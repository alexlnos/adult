<p>
<b>
										Higit sa 20 cm:
									</b> ito ay ganap na malaking sukat. Kahit marami ang mga lalaki na ipinagmamalaki na mayroon silang ganitong titi, ang laki na ito ay napakabihira. Walang 1% ng tao ang may ganito. Gayunpaman, ang mga ganitong lalaki ay palaging hinahanap at hinahabol ng mga babae, at pinag-uusapan dahil sa laki.
                        </p>
<p>
<b>
										14-16 cm:
									</b> ito ang kasiya-siyang laki ng titi. Sa kabila ng salitang "kasiya-siya", kadalasan ay lubos na mahirap pa rin na gawing masaya ang babae sa sukat na ito. Gayunpaman, mula sa medikal na pananaw, ang laki na ito ay ganap na normal.
